package cn.itbeien.common.service;

import cn.itbeien.common.constant.CacheConstants;
import cn.itbeien.common.entity.SysUser;
import cn.itbeien.common.exception.user.UserPasswordNotMatchException;
import cn.itbeien.common.exception.user.UserPasswordRetryLimitExceedException;
import cn.itbeien.common.redis.RedisCache;
import cn.itbeien.common.security.context.AuthenticationContextHolder;
import cn.itbeien.common.util.SecurityUtils;
import lombok.RequiredArgsConstructor;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.security.core.Authentication;
import org.springframework.stereotype.Component;

import java.util.concurrent.TimeUnit;

